/**************************************************
 * (a * b) mod c
 **************************************************/
// 当乘法取模可能会爆int64时，手写乘法取模以避免溢出 
ll ProductMod(ll a, ll b, ll c) {
	a %= c; b %= c;
	ll r = 0;
	for (; b; a = (a << 1) % c, b >>= 1)
		if (b & 1) r = (r + a) % c;
	return r;
}
