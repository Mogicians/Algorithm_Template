/*************************************************************************
 * 乘法逆元：若 ax ≡ 1 (mod n)，则 x称为a对n的乘法逆元
 * 用处：在取模时将除法转化为乘法
 *   例：若 A*X=1(mod C)有解且A|F(F%A==0),
 *       则 (F/A) mod C = (F/A)*(A*X) mod C = (F*X) mod C
 *************************************************************************/
// n必须大于0，且a与n必须互质 
int InvMod(int a, int n) {
	int x, y;
	if (extgcd(a, n, x, y) != 1) return -1;
	return (x % n + n) % n;
}
